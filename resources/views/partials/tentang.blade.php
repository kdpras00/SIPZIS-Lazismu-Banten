                document.addEventListener('DOMContentLoaded', function() {
                    // Optimized toggle function with better performance
                    function setupToggle(toggleId, contentId, chevronId) {
                        const toggle = document.getElementById(toggleId);
                        const content = document.getElementById(contentId);
                        const chevron = document.getElementById(chevronId);

                        // Initially hide content
                        content.style.display = 'none';

                        toggle.addEventListener('click', function() {
                            // Use display instead of max-height for better performance
                            if (content.style.display === 'none') {
                                content.style.display = 'block';
                                chevron.style.transform = 'rotate(180deg)';
                            } else {
                                content.style.display = 'none';
                                chevron.style.transform = 'rotate(0deg)';
                            }
                        });
                    }

                    // Setup toggles
                    setupToggle('toggle-mission', 'mission-content', 'mission-chevron');
                    setupToggle('toggle-principle', 'principle-content', 'principle-chevron');
                    setupToggle('toggle-goal', 'goal-content', 'goal-chevron');
